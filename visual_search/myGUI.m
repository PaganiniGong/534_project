% --- Executes on button press in pushbutton3.
function pushbutton3_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton3 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
[filename pathname] = uigetfile({'*.jpg';'*.bmp';'*.*'},'upload');
str = [pathname filename];
index = filename(4:end - 4);
global indexnum;
indexnum = str2num(index);
im = imread(str); 
axes(handles.axes1); 
imshow(im);
S = load('statxture.mat');
StatxtureData = S.Istatxture;
order = zeros(1,2000);

RetrieveImg = indexnum;   
for i=1:2000
    order(1,i)= CalDis(StatxtureData(:,RetrieveImg),StatxtureData(:,i));
end
[sA,index] = sort(order) ;

for i=1:10
    I=imread(['pic',num2str(index(i)) ,'.jpg']);
    axesnum = i - 1 + 10;
    switch axesnum
        case 10
             axes(handles.axes10)
        case 11
             axes(handles.axes11)
        case 12
             axes(handles.axes12)
        case 13
             axes(handles.axes13)
        case 14
             axes(handles.axes14)
        case 15
             axes(handles.axes15)
        case 16
             axes(handles.axes16)
        case 17
             axes(handles.axes17)
        case 18
             axes(handles.axes18)
        case 19
             axes(handles.axes19) 
    end
imshow(I);
end
